/*Инструкция if - заставляют интерпретатор выполнять или пропускать другие инструкции,
в зависимости от значения определенного выражения*/
let a = 10;
let b = 20;

if(a = 10) {               
    let c = a + b;        //В скобках указывается выражение.
    alert(c);             //Если при его вычислении будет возвращено true, то код будет выполнен 
}


if (a > 10) {
    alert('a > 10')
}
else {
    alert('else')
}


